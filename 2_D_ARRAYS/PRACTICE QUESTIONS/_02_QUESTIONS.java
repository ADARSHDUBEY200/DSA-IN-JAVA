

public class _02_QUESTIONS {

    public static void main(String[] args) {
        
        int[][] arr={
            {1,4,9},
            {11,4,3},
            {2,2,3}

        };

        int sum=0;

        for(int i=1;i==1;i++){
            for(int j=0;j<=arr[0].length-1;j++){

                sum+=arr[i][j];

            }
        }

        System.out.println("THE SUM OF THE SECOND INDEX OF YOUR 2-D ARRAY IS : "+sum);
    }
    
}
