

public class _01_OUTPUT {

    public static void main(String[] args) {
        int a=10;
        char b='a';
        String c="My name is Adarsh Dubey";
        boolean isFound =true;
// HERE WE USE THE PRINTLN FUNCTION IN JAVA FOR PRINTING THE OUTPUT IN NEXT LINE 
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(isFound);
// WE CAN ALSO USE THE PRINT FUNCTION IN JAVA THEN THE OUTPUT IS PRINTED IN THE SAME LINE 
        System.out.print(a);
        System.out.print(b);
        System.out.print(c);
        System.out.print(isFound);

// IF WE WANT TO RUN OUR JAVA PROGRAM THROUGH THE TERIMNAL DIRECTLY THEN WE USE THE TWO COMMANDS WITHOUT USING THE CODE RUNNER :

    // 1. javac _01_OUTPUT.java // THIS USED FOR THE COMPILATION PROCESS
    // 2. java  _01_OUTPUT.java // THIS IS USED FOR THE RUN THE FILE AND    PRINTING THE OUTPUT ON THE TERMINAL 
    }
    
}
