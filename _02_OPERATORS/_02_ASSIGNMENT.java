package _02_OPERATORS;

public class _02_ASSIGNMENT {
    public static void main(String[] args) {
        int a = 1;
        a += a; // a = a + a
        a -= a; // a = a - a
        a *= a; // a = a * a
        a /= a; // a = a / a
        a %= a; // a = a % a

        System.out.println(a);

    }
    
}
