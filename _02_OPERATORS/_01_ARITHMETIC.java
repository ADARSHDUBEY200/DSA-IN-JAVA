

public class _01_ARITHMETIC {
    public static void main(String args []){
        int a = 5;
        int b = 4;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);

        int c = 4;
        System.out.println(++c); // 5
        System.out.println(c++); // 5
        System.out.println(--c); // 5 
        System.out.println(c--); // 5 
    }
}
